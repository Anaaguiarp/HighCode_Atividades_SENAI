public class Calculadora {
    public static double somar(int a, int b){
        return a + b;
    }

    public static double subtrair(int a, int b){
        return a - b;
    }

    public static double multiplicar(int a, int b){
        return a * b;
    }

    public static double dividir(int a, int b){
        return a / b;
    }
}
