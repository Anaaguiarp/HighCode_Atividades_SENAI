public class Curso {
    private String nome;
    private double nota;

    public Curso(String nome, double nota) {
        this.nome = nome;
        this.nota = nota;
    }

    public String getNome() {
        return nome;
    }

    public double getNota() {
        return nota;
    }

    public void setNota(double nota) {
        this.nota = nota;
    }

    @Override
    public String toString() {
        return nome + " (Nota: " + nota + ")";
    }
}